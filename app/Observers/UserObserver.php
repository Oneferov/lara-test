     * Handle the User "updated" event.
     *
     * @param  \App\Models\User  $user
     * @return void
     */
    public function updated(User $user)
    {
        $changes = $user->getChanges();
        if(array_key_exists('user_type_id', $changes) || array_key_exists('position_id', $changes)) {

            Mail::to($user->email)->send(new ChangeUserMail($user));
        }
    }

